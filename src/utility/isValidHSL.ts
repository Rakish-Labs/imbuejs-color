export const isValidHSL = (alleged: string): boolean => {
  const hslRegex =
    /^hsl\(((((([12]?[1-9]?\d)|[12]0\d|(3[0-5]\d))(\.\d+)?)|(\.\d+))(deg)?|(0|0?\.\d+)turn|(([0-6](\.\d+)?)|(\.\d+))rad)((,\s?(([1-9]?\d(\.\d+)?)|100(\.0)?|(\.\d+))%){2}|(\s(([1-9]?\d(\.\d+)?)|100(\.0)?|(\.\d+))%){2})\)$/
  const hslaRegex =
    /^hsla\(((((([12]?[1-9]?\d)|[12]0\d|(3[0-5]\d))(\.\d+)?)|(\.\d+))(deg)?|(0|0?\.\d+)turn|(([0-6](\.\d+)?)|(\.\d+))rad)(((,\s?(([1-9]?\d(\.\d+)?)|100(\.0)?|(\.\d+))%){2},\s?)|((\s(([1-9]?\d(\.\d+)?)|100(\.0)?|(\.\d+))%){2}\s\/\s))((0?\.\d+)|[01]|(([1-9]?\d(\.\d+)?)|100(\.0)?|(\.\d+))%)\)$/i

  return [hslRegex, hslaRegex].some(
    (regex: RegExp) => alleged.match(regex) !== null,
  )
}
